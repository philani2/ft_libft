#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*str;
	int		j;

	j = ft_strlen((char*)s1);
	i = 0;
	str = (char *)malloc(sizeof(char) * ft_strlen((char*)s1) + 1);
	if (str == NULL)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
